static void render_status(void) {
    // Host Keyboard Layer Status
    oled_write_P(PSTR("Amon Types\n\nLAYER: "), false);
    switch (get_highest_layer(layer_state)) {
        case _QWERTY:
            oled_write_P(PSTR("QWERTY\n\n"), false);
            break;
        case _2WERTY:
            oled_write_P(PSTR("NUM/SYMBOLS\n\n"), false);
            break;
        case _LOWER:
            oled_write_P(PSTR("SYMB/ARROWS\n\n"), false);
            break;
        case _RAISE:
            oled_write_P(PSTR("NUM/MEDIA\n\n"), false);
            break;
        case _ADJUST:
            oled_write_P(PSTR("FUNCTION/RGB\n\n"), false);
            break;
        default:
            oled_write_P(PSTR("Undefined "), false);
            break;
    }
    oled_write_P(PSTR("Die before you die,\nthere is no chance\nafter."), false);

    //Host Keyboard LED Status
    led_t led_state = host_keyboard_led_state();
    oled_write_P(led_state.num_lock ? PSTR("NUM -") : PSTR("       "), false);
    oled_write_P(led_state.caps_lock ? PSTR("CAPS ") : PSTR("       "), false);
    oled_write_P(led_state.scroll_lock ? PSTR("SCRL") : PSTR("       "), false);
    oled_write_P(is_page_scroll_layer ? PSTR("Scroll") : PSTR("       "), false);
    oled_write_P(is_alt_tab_layer ? PSTR("Alt Tab") : PSTR("       "), false);
}


